    public void drawGrid2D(Graphics g) {
        grapWidth = jPanel_VeToaDo2D.getBounds().width;
        grapHeight = jPanel_2D.getBounds().height;
        g.setColor(Color.LIGHT_GRAY);
        for (int i = 5; i < grapWidth; i += 5) {
            g.drawLine(i, 0, i, grapHeight);
        }
        for (int i = 5; i < grapHeight; i += 5) {
            g.drawLine(0, i, grapWidth, i);
        }
    }

    public void drawOxy(Graphics g)
    {
        grapWidth = jPanel_VeToaDo2D.getBounds().width;
        grapHeight = jPanel_2D.getBounds().height;
        g.setColor(Color.BLACK);
        g.drawLine(grapWidth / 2, 0, grapWidth / 2, grapHeight); // ve truc tung
        g.drawLine(0, grapHeight / 2, grapWidth, grapHeight / 2); // ve truc hoanh

        for (int i = grapWidth / 2; i < grapWidth; i += step) {
            g.drawLine(i, grapHeight / 2 - 2, i, grapHeight / 2 + 2); // ve gach truc Ox duong
        }
        for (int i = grapWidth / 2 - step; i > 0; i -= step) {
            g.drawLine(i, grapHeight / 2 - 2, i, grapHeight / 2 + 2); // ve gach truc Ox am
        }
        for (int i = grapHeight / 2; i > 0; i -= step) {
            g.drawLine(grapWidth / 2 - 2, i, grapWidth / 2 + 2, i); // ve gach truc Oy duong
        }
        for (int i = grapHeight / 2 + step; i < grapHeight; i += step) {
            g.drawLine(grapWidth / 2 - 2, i, grapWidth / 2 + 2, i); // ve  gach truc Oy am
        }
        g.drawLine(grapWidth - 10, grapHeight / 2 - 5, grapWidth, grapHeight / 2);
        g.drawLine(grapWidth - 10, grapHeight / 2 + 5, grapWidth, grapHeight / 2);
        g.drawLine(grapWidth / 2 - 5, 10, grapWidth / 2, 0);
        g.drawLine(grapWidth / 2 + 5, 10, grapWidth / 2, 0);

        g.drawString("x", grapWidth - 7, grapHeight / 2 + 20);
        g.drawString("y", grapWidth / 2 + 5, 20);
        g.drawString("O", grapWidth / 2 + 5, grapHeight / 2 + 20);

    }

